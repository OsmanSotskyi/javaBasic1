package class2;

public class T4ArithmeticOperators {
    public static void main(String[] args) {
        int num1=6;
        int num2=3;
        double addResult=num1+num2;
        System.out.println("The addition of 2 numbers "+num1+" and "+num2+" is equal to "+addResult);

        int subtraction=num1-num2;
        System.out.println("The subtract of 2 numbers "+num1+" and "+num2+" is equal to "+subtraction);

        int multiply=num1*num2;
        System.out.println("The multiply of 2 numbers "+num1+" and "+num2+" is equal to "+multiply);

        double division= num1/num2;
        System.out.println("The division of 2 numbers "+num1+" and "+num2+" is equal to "+division);

        double num3=3.9;
        double square=num3*num3;
        System.out.println("The square of the 3.9 is "+square);

        int width=5;
        int height=8;
        int perimeter=width*height;
        System.out.println("the perimeter of a rectangle with width "+width+" and height "+height+" is equal to "+perimeter+" and the area is "+perimeter);



    }
}
