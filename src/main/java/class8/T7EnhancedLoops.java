package class8;

public class T7EnhancedLoops {
    public static void main(String[] args) {
        int[] nums = {10, 23, 25, 56, 45, 20,-10,-5};

        for (int i = 0; i < nums[i]; i++) {
            if(nums[i]<0){
                nums[i]=0;
            }
            System.out.println(nums[i]);

        }
    }
}
