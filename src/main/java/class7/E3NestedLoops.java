package class7;

public class E3NestedLoops {
    public static void main(String[] args) {

        for (int j=0; j<3;j++){            //kollichestvo liniy
            for ( int i=2;i<20;i=i+2){       //kollichestvo znacheniy v odnoy linii
                System.out.print(i+" ");
            }
            System.out.println();

        }
    }
}
